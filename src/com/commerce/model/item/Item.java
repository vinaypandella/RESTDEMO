package com.commerce.model.item;



public class Item {

	
	private String styleColorNumber;
	
	private String style;
	
	private String description;
	
	private int price;
	
	private String color;

	public String getStyleColorNumber() {
		return styleColorNumber;
	}

	public void setStyleColorNumber(String styleColorNumber) {
		this.styleColorNumber = styleColorNumber;
	}

	public String getStyle() {
		return style;
	}

	public void setStyle(String style) {
		this.style = style;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}
}
